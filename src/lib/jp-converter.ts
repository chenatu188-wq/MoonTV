/**
 * 把繁體字串轉成日文新字體
 * 字典外的字不變
 */
export function toJapanese(text: string): string {
  if (!text) return text;
  let result = '';
  for (const ch of text) {
    result += TW_TO_JP[ch] ?? ch;
  }
  return result;
}
