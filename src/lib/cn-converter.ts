/**
 * 把繁體字串轉成簡體（影視站搜尋用）
 * 字典外的字不變
 */
export function toSimplified(text: string): string {
  if (!text) return text;
  let result = '';
  for (const ch of text) {
    result += TW_TO_CN[ch] ?? ch;
  }
  return result;
}
